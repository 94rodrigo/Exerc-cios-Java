package streams;

public class Carro {
	final String marca;
	final String modelo;
	final String categoria;
	final double preco;
	final boolean pinturaMetalica;
	
	public Carro(String marca, String modelo, String categoria, double preco, boolean pinturaMetalica) {
		this.marca = marca;
		this.modelo = modelo;
		this.categoria = categoria;
		this.preco = preco;
		this.pinturaMetalica = pinturaMetalica;
	}
	
}
